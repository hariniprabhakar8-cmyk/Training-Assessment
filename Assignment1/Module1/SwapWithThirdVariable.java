package Module1;

public class SwapWithThirdVariable {
        public static void main(String[] args) {
            int a=7;
            int b=6;
            int temp;
            temp=a;
            a=b;
            b=temp;
            System.out.println("The swapped numbers are:");
            System.out.println(a);
            System.out.println(b);
        }
    }
